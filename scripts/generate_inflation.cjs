const data = {};
let pureIndex = 1;

// 1. Generate Synthetic Monthly Rates derived from valid Annual Data for 1950 - 1991
for (let year = 1950; year <= 1991; year++) {
    const annualRate = annualInflationRates[year] / 100;
    const monthlyMultiplier = Math.pow(1 + annualRate, 1 / 12);

    for (let month = 1; month <= 12; month++) {
        data[`${year}-${month}`] = {
            pureIndex: pureIndex
        };
        pureIndex *= monthlyMultiplier;
    }
}

// 2. Map direct real monthly data for 1992 - Present
for (let year = 1992; year <= 2027; year++) {
    for (let month = 1; month <= 12; month++) {
        const key = `${year}-${month}`;
        if (monthlyInflationRates[key] !== undefined) {
            data[key] = { pureIndex: pureIndex };
            pureIndex *= (1 + (monthlyInflationRates[key] / 100));
        } else {
            // Future projection for any future date missing from the static array, default to 2.5%
            data[key] = { pureIndex: pureIndex };
            pureIndex *= 1.025;
        }
    }
}

// Calculate true values considering currency changes
for (const [key, value] of Object.entries(data)) {
    const [year, month] = key.split('-').map(Number);

    let multiplierToARS = 1;

    if (year < 1970) {
        multiplierToARS = 10000000000000;
    } else if (year < 1983 || (year === 1983 && month < 6)) {
        multiplierToARS = 100000000000;
    } else if (year < 1985 || (year === 1985 && month < 6)) {
        multiplierToARS = 10000000;
    } else if (year < 1992) {
        multiplierToARS = 10000;
    } else {
        multiplierToARS = 1;
    }

    data[key].adjustedIndex = value.pureIndex * multiplierToARS;
}

fs.writeFileSync(path.join(__dirname, '../src/data/inflation.json'), JSON.stringify(data, null, 2));
